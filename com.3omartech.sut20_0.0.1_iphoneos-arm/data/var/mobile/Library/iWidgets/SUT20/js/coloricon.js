function getIconInfo(number, info){
  var hours = new Date().getHours(),
    isDayTime = hours > 6 && hours < 20;

  if(iconInfo[number]){
    if(number == 26 && !isDayTime){
      if(info === 'color'){
        return 'light';
      }else if(info === 'imageName'){
        return 'sunnynight';
      }else {
        return iconInfo[number][info];
      }
    }else{
      return iconInfo[number][info];
    }
  }
}